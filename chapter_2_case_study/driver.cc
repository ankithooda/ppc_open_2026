int main (int argc, char **argv) {

    std::string small = "small";
    std::string large = "large";
    std::string mid = "mid";

    if (argv[1] == small) {
        small_test();
    } else if (argv[1] == large){
        large_test();
    } else if (argv[1] == mid) {
        mid_test();
    } else {
        std::cerr << "Invalid test suite name " << argv[0] << std::endl;
        return 1;
    }
  return 0;
}

void small_test() {
    constexpr int n = 3;
    const float d[n*n] = {
        0, 8, 2,
        1, 0, 9,
        4, 5, 0,
    };
    float r[n*n];
    step(r, d, n);
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            std::cout << r[i*n + j] << " ";
        }
        std::cout << "\n";
    }
}

